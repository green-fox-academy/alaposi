public class TwoNumbers {
    public static void main(String[] args) {
        System.out.println(13+22);
        System.out.println(22-13);
        System.out.println(22*13);
        System.out.println(22./13);
        System.out.println(22/13);
        System.out.println(22%13);


        int a = 22;
        int b = 13;
        float c = (float)a / b;
        System.out.println(c);


    }

}
